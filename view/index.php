                    1967 la gobernación de Santander mediante decreto N°. 0751 del mes de abril, convierte la Escuela 
                    Vocacional Agrícola en el colegio Bachillerato Clásico debido a que no existían los recursos necesarios 
                    para desarrollar la modalidad agrícola. La institución, entonces, retoma la denominación de Balbino 
                    García y pasa a funcionar con los cursos 1° a 4° de Bachillerato en el local donde ahora es la Sede B del 
